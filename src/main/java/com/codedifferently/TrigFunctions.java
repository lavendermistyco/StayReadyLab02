package com.codedifferently;

public class TrigFunctions {

    //default mode is in degrees

    public double sine(){
        return SciCalculator.currValue = Math.sin(SciCalculator.currValue);

    }

    public double cosine(){

        return SciCalculator.currValue = Math.cos(SciCalculator.currValue);

    }

    public double tangent(){

        return SciCalculator.currValue = Math.tan(SciCalculator.currValue);

    }
    
    public double inverseSine(){

        return SciCalculator.currValue = Math.asin(SciCalculator.currValue);

    }

    public double inverseCosine(){

        return SciCalculator.currValue = Math.acos(SciCalculator.currValue);

    }

    public double inverseTan(){

        return SciCalculator.currValue = Math.atan(SciCalculator.currValue);

    } 
}